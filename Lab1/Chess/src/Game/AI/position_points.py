def get_position_points(piece):
    result = []

    if piece.symbol == 'r':
        result = rook

    elif piece.symbol == 'q':
        result = Queen

    elif piece.symbol == 'b':
        result = bishop

    elif piece.symbol == 'n':
        result = knight

    elif piece.symbol == 'k':
        result = king

    else:
        result = pawn

    return result if piece.color == 'w' else np.flip(result) * (-1)
